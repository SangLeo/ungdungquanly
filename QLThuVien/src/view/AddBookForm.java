    /**
     * Use to check data input
     * @return true if data input is valid else not valid 
     */
    public boolean checkValid(){
        if( masach_text.getText().isEmpty()
                || tensach_text.getText().isEmpty()
                || tacgia_text.getText().isEmpty()
                || nxb_text.getText().isEmpty()
                || namxuatban_text.getText().isEmpty()
                || ISBN_text.getText().isEmpty()
                || soluong_text.getText().isEmpty()
                )
        {
            return false;
        }
        else
        {
            return true;
        }       
    }
