    private void them_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_them_buttonActionPerformed
        if(!checkValid()|| bookController.checkBookExists(masach_text.getText())){
            JOptionPane.showMessageDialog(this, "Bạn đã nhập thiếu thông tin hoặc mã sách nhập vào đã tồn tại!");
            
        }
        else{
            JOptionPane.showMessageDialog(this, "Dữ liệu nhập vào hợp lệ, đã thêm sách!");
            String id = masach_text.getText();
            String name = tensach_text.getText();
            String author = nxb_text.getText();
            String publishing_company = tacgia_text.getText();
            String publishing_date = namxuatban_text.getText();
            int totalQuantity = Integer.parseInt(soluong_text.getText());
            String ISBN = ISBN_text.getText();
            int currentQuantity = totalQuantity;
            BooksController bookController = new BooksController();
            bookController.addBook(new Book(id, name, author, publishing_company, publishing_date, ISBN, totalQuantity, currentQuantity));
        }
    }//GEN-LAST:event_them_buttonActionPerformed

