    private void them_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_them_buttonActionPerformed
        if(!checkValid()|| bookController.checkCopyExists(macopy_text.getText())){
            JOptionPane.showMessageDialog(this, "Bạn đã nhập thiếu thông tin hoặc mã copy đã tồn tại!");
        }
        else{
            JOptionPane.showMessageDialog(this, "Dữ liệu nhập vào hợp lệ, đã thêm bản copy!");
            String idBook = masach_combobox.getSelectedItem().toString();
            String idCopy = macopy_text.getText();
            String danhmuc = danhmuc_text.getText();
            String trangthai = trangthai_combobox.getSelectedItem().toString();
            int giatien = Integer.parseInt(giatien_text.getText());
            BooksController bookController = new BooksController();
            bookController.addCopy(new Copy(idCopy,idBook, danhmuc, giatien, trangthai));
            bookController.increaseTotalQuantity(1, idBook);
        }
    }//GEN-LAST:event_them_buttonActionPerformed

