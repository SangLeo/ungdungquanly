    private void giatien_textKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_giatien_textKeyTyped
        // TODO add your handling code here:
        char vchar = evt.getKeyChar();
        if(!(Character.isDigit(vchar))|| vchar == KeyEvent.VK_BACKSPACE || vchar==KeyEvent.VK_DELETE){
            evt.consume();
        }
    }//GEN-LAST:event_giatien_textKeyTyped

