    private void cancelItem_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cancelItem_buttonMouseClicked
        if (rowTableOrder == -1){
            int ret = JOptionPane.showConfirmDialog(null, "Bạn chưa chọn item" ,"THÔNG BÁO", JOptionPane.YES_NO_OPTION);
        }
        else {
            int ret = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn hủy?" ,"THÔNG BÁO", JOptionPane.YES_NO_OPTION);
            if(ret == JOptionPane.YES_OPTION){
                int idItem = (int) itemOrdering_table.getModel().getValueAt(rowTableOrder, 0);
                String idBook = itemOrdering_table.getModel().getValueAt(rowTableOrder, 1).toString();
                controller.cancelOrderBookItem(idItem,idBook, idBorrower);
                updateView();
                rowTableOrder = -1;
            }

        }

    }//GEN-LAST:event_cancelItem_buttonMouseClicked

