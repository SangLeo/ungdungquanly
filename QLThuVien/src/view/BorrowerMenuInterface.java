    private void orderBook_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_orderBook_buttonMouseClicked
        if (rowTableBook == -1){
            int ret = JOptionPane.showConfirmDialog(null, "Bạn chưa chọn sách" ,"THÔNG BÁO", JOptionPane.YES_NO_OPTION);
        }
        else{
            OrderBookController controller = new OrderBookController();
            String idBook = book_table.getModel().getValueAt(rowTableBook, 0).toString();
            controller.orderBook(Book.getBook(idBook), BorrowerInfo.getBorrower(idBorrower));
            rowTableBook = -1;
            updateView();
        }

    }//GEN-LAST:event_orderBook_buttonMouseClicked

