    private void xoacopy_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoacopy_btnActionPerformed
        int row = listCopy.getSelectedRow();
        if(row < 0)
        {
            JOptionPane.showMessageDialog(this,"Bạn chưa chọn hàng nào!");
        }
        else
        {  
            JOptionPane.showMessageDialog(this, "Bạn đã xóa bản sao thành công!");
            BooksController bookController = new BooksController();
            bookController.deleteCopy(macopy_combobox.getSelectedItem().toString());
            System.out.println();
            bookController.decreaseTotalQuantity(1, listCopy.getModel().getValueAt(row, 1).toString());
        }
    }//GEN-LAST:event_xoacopy_btnActionPerformed

