    public void showCopy(){
        ArrayList<Copy> list = copyDao.getCopyFromId(macopy_combobox.getSelectedItem().toString());
        DefaultTableModel model = (DefaultTableModel) listCopy.getModel();
        Object[] row = new Object[8];
        for(int i = 0;i < list.size();i ++){
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getIdBook();
            row[2] = list.get(i).getCategory();
            row[3] = list.get(i).getPrice();
            row[4] = list.get(i).getStatus();
            model.addRow(row);
        } 
    }
