   public void showBooks(){
        ArrayList<Book> list = bookController.getBookFromId(masach_combobox.getSelectedItem().toString());
        DefaultTableModel model = (DefaultTableModel) listBooks.getModel();
        Object[] row = new Object[8];
        row[0] = list.get(0).getId();
        row[1] = list.get(0).getName();
        row[2] = list.get(0).getAuthor();
        row[3] = list.get(0).getPublishingCompany();
        row[4] = list.get(0).getPublishingDate();
        row[5] = list.get(0).getISBN();
        row[6] = list.get(0).getTotalQuantity();
        row[7] = list.get(0).getCurrentQuantity();
        model.addRow(row);
        }
