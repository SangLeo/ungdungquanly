    private void sua_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sua_btnActionPerformed
        int row = listBooks.getSelectedRow();
        if(row < 0)
        {
            JOptionPane.showMessageDialog(this,"Bạn chưa chọn hàng nào!");
        }
        else
        {  
           DefaultTableModel model = (DefaultTableModel) listBooks.getModel();
           JOptionPane.showMessageDialog(this, "Bạn đã sửa thành công!");
           model.setValueAt(tensach_text.getText(), row, 1);
           model.setValueAt(tacgia_text.getText(), row, 2);
           model.setValueAt(nxb_text.getText(), row, 3);
           model.setValueAt(Ngayxuatban_text.getText(), row, 4);
           model.setValueAt(isbn_text.getText(), row, 5);
           model.setValueAt(soluong_text.getText(), row, 6);
           
        }
    }//GEN-LAST:event_sua_btnActionPerformed

