    private void listCopyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listCopyMouseClicked
        DefaultTableModel model = (DefaultTableModel)listCopy.getModel();
        macopy_text.setText(model.getValueAt(listCopy.getSelectedRow(), 0).toString());
        masach_text.setText(model.getValueAt(listCopy.getSelectedRow(), 1).toString());
        danhmuc_text.setText(model.getValueAt(listCopy.getSelectedRow(), 2).toString());
        giatien_text.setText(model.getValueAt(listCopy.getSelectedRow(), 3).toString());
        trangthai_text.setText(model.getValueAt(listCopy.getSelectedRow(), 4).toString());
    }//GEN-LAST:event_listCopyMouseClicked

    private void sua_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sua_btnActionPerformed
        int row = listCopy.getSelectedRow();
        if(row < 0)
        {
            JOptionPane.showMessageDialog(this,"Bạn chưa chọn hàng nào!");
        }
        else
        {
            DefaultTableModel model = (DefaultTableModel) listCopy.getModel();
            JOptionPane.showMessageDialog(this, "Bạn đã sửa thành công!");
            model.setValueAt(masach_text.getText(), row, 1);
            model.setValueAt(danhmuc_text.getText(), row, 2);
            model.setValueAt(giatien_text.getText(), row, 3);
            model.setValueAt(trangthai_text.getText(), row, 4);
        }
    }//GEN-LAST:event_sua_btnActionPerformed

