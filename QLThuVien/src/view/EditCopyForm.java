   public void showBooks(){
        ArrayList<Copy> list = bookController.getCopyFromId(macopy_combobox.getSelectedItem().toString());
        DefaultTableModel model = (DefaultTableModel) listCopy.getModel();
        Object[] row = new Object[6];
        row[0] = list.get(0).getId();
        row[1] = list.get(0).getIdBook();
        row[2] = list.get(0).getCategory();
        row[3] = list.get(0).getPrice();
        row[4] = list.get(0).getStatus();
        model.addRow(row);
        }
